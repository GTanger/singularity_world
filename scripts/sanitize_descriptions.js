function clean(desc) {
  if (!desc || typeof desc !== 'string') return desc;
  let s = desc;
  for (const [re, repl] of RULES) {
    s = s.replace(re, repl);
  }
  // 清理多餘標點與空白
  s = s.replace(/\s*，\s*，/g, '，').replace(/\s*。\s*。/g, '。').replace(/。，/g, '。').replace(/\s+/g, ' ').replace(/\s*，\s*$/g, '。').trim();
  return s;
}

let count = 0;
data.rooms.forEach((r) => {
  const before = r.description;
  const after = clean(before);
  if (before !== after) {
    r.description = after;
    count++;
  }
});

fs.writeFileSync(ROOMS_JSON, JSON.stringify(data, null, 2) + '\n', 'utf8');
console.log('已修正', count, '筆房間描述。');
